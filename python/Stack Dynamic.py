# Stack Dynamic Array Example
# This code demonstrates how to create a dynamic array in Python
# using a list and how to initialize it with a specific size.
# The array is initialized with zeros, and then each element is set to its index multiplied by 10.
# This is a simple example of a stack dynamic array in Python.
# Stack Dynamic Array Example



arr = [0] * 5  
for i in range(5):
    arr[i] = i * 10  # Initialize each element with its index multiplied by 10
# Print the array
print(arr)
